        <p class="about">BORDEAUX BASKET est le survivant de W. BORDEAUX BASKET qui a du s'arrêter le 12.09.2003.</p>
        Le passé de W. <span class="text-primary">BORDEAUX BASKET</span> remonte à la création d'un premier club en 1935 sous le nom de SPORTING CLUB BORDELAIS. <br> Ce club a subsisté en dépit de la seconde guerre mondiale et en 1982 accède
        à la Nationale 1 (plus haut niveau de l'époque).
        <br> En 1989 (après une fusion avec la Maison de Quartier CHANTECLER), le club prend le nom de SPORTING CHANTECLER BORDEAUX, puis en 1996 grâce à WILLIAM PITTERS qui est le sponsor principal, il devient WAÏTI BORDEAUX CHANTECLER.
        <br> C'est alors que la Mairie de BORDEAUX exige la séparation entre "les Maisons de Quartiers" et "les clubs de Haut Niveau" le club s'appellera désormais WAÏTI BORDEAUX BASKET.
        <br> En 2001, la boisson WAÏTI devient alcoolisée, la "loi Evin" oblige : WAÏTI BORDEAUX BASKET se transforme en W. <span class="text-primary">BORDEAUX BASKET</span>.
        <br> Depuis 1982, le club s'est maintenu au plus haut niveau et c'est le doyen du Championnat de France de la LIGUE FEMININE et le seul représentant "Elite" du sport en salle à Bordeaux. Mais le sort en a décidé autrement, tout s'écroule
        le 12.09.2003.
        <p> Les dirigeants ne veulent pas abandonner les 58 enfants licenciés et prennent la décision de créer un nouveau club.
            <br> C'est ainsi qu'est néé le 19 Septembre 2003 un nouveau club : BORDEAUX BASKET avec de nouveaux dirigeants (les parents des joueurs et joueuses) qui s'impliquent avec enthousiasme et plaisir.
            <br> Depuis ce jour le nombre de licenciés grandit tous les ans. Nous avons aujourd'hui 14 équipes engagées dans les divers championnats Départementaux et Régionaux.
            <br> Nous avons fêté les 10 ans du club en 2013. Par la suite à la demande d'une grande majorité de licenciés ainsi que des membres du Bureau nous avons décidé de préciser la localisation du club afin de mieux représenter notre
            quartier. C'est ainsi qu'à l'Assemblée Générale du mois de Juin 2015, il a été voté notre nouvelle appellation : BORDEAUX BASTIDE BASKET
        </p>
